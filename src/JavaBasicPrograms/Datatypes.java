package JavaBasicPrograms;

public class Datatypes {

    public static void main(String[] args){

        byte b = -9;  //size = 1 byte (range -128 to 127)
        char c = 'A'; //size = 2 bytes
        short s = 2198; //size = 2 bytes
        int i = 1050; //size = 4 bytes
        long l = 29345879L; //size = 8 bytes
        float f = 5.934f; //size = 4 bytes
        double d = 82.3846989346d; //size = 8 bytes
        boolean isTorF = true;

        System.out.println("Byte : "+b);
        System.out.println("Char : "+c);
        System.out.println("Short : "+s);
        System.out.println("Int : "+i);
        System.out.println("Long : "+l);
        System.out.println("Float : "+f);
        System.out.println("Double : "+d);
        System.out.println("Boolean : "+isTorF);

    }
}
